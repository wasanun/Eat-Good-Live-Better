package com.example.eatgood;

public class Wei {
    private String Name;
    private  String Cal;

    public Wei() {
    }

    public Wei(String name, String cal) {
        Name = name;
        Cal = cal;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getCal() {
        return Cal;
    }

    public void setCal(String cal) {
        Cal = cal;
    }
}
